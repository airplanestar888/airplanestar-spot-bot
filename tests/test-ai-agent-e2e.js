require("dotenv").config();
const fs = require("fs");
const path = require("path");
const axios = require("axios");

const config = JSON.parse(fs.readFileSync(path.join(__dirname, "..", "config.json"), "utf8"));
const aiAgent = require(path.join(__dirname, "..", "core", "aiAgent.js"));

async function main() {
  const apiKey = process.env.OPENROUTER_API_KEY;
  const model = process.env.OPENROUTER_MODEL || "openai/gpt-5-mini";
  if (!apiKey) throw new Error("OPENROUTER_API_KEY missing");

  const rotation = config.lastAutoPairRotation || {
    activePairs: config.pairs || [],
    topPairs: 15,
    activeCategories: "bestVolume, bestMomentum, notOverextended"
  };
  const candidates = Array.isArray(rotation.candidates) ? rotation.candidates : null;
  if (!candidates || !candidates.length) {
    throw new Error("No auto-rotate candidates found. This test only accepts real candidates generated by auto-rotate.");
  }

  const prompt = aiAgent.__buildPromptForTest
    ? aiAgent.__buildPromptForTest({ config, rotation, candidates })
    : null;

  if (!prompt) throw new Error("buildPrompt test export unavailable");

  const res = await axios.post(
    "https://openrouter.ai/api/v1/chat/completions",
    {
      model,
      messages: [
        {
          role: "system",
          content: "Return one valid JSON object only, with no markdown and no extra text."
        },
        {
          role: "user",
          content: prompt
        }
      ],
      response_format: { type: "json_object" },
      max_tokens: 5000,
      temperature: 0.2
    },
    {
      timeout: 30000,
      proxy: false,
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
        "HTTP-Referer": "https://airplanestar.local",
        "X-Title": "airplanestar-bot"
      }
    }
  );

  const rawText = res.data?.choices?.[0]?.message?.content || "";
  const parsed = aiAgent.__parseJsonForTest ? aiAgent.__parseJsonForTest(rawText) : null;
  const settings = aiAgent.getAiAgentSettings(config);
  let validated = null;
  let validationError = null;
  try {
    validated = aiAgent.validateDecision(parsed, settings);
  } catch (err) {
    validationError = err.message;
  }

  console.log(JSON.stringify({
    provider: res.data?.provider,
    model: res.data?.model,
    finish_reason: res.data?.choices?.[0]?.finish_reason,
    rawText,
    parsed,
    validated,
    validationError
  }, null, 2));
}

main().catch((err) => {
  console.error(err.response?.data || err.message || err);
  process.exit(1);
});
