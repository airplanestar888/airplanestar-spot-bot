require("dotenv").config();
const axios = require("axios");
const path = require("path");
const aiAgent = require(path.join(__dirname, "..", "core", "aiAgent.js"));
const config = require(path.join(__dirname, "..", "config.json"));

async function main() {
  const apiKey = process.env.OPENROUTER_API_KEY;
  const model = process.env.OPENROUTER_MODEL || "openai/gpt-5-mini";
  if (!apiKey) throw new Error("OPENROUTER_API_KEY missing");

  const rotation = config.lastAutoPairRotation || {
    activePairs: config.pairs || [],
    topPairs: 15,
    activeCategories: "-"
  };
  const candidates = Array.isArray(rotation.candidates) ? rotation.candidates : null;
  if (!candidates || !candidates.length) {
    throw new Error("No auto-rotate candidates found. This test only accepts real candidates generated by auto-rotate.");
  }

  const richContext = {
    role: {
      persona: "Senior crypto spot trader in 2026",
      objective: [
        "Preserve capital first",
        "Match market regime with active entry style",
        "Allow entries only when market quality fits the bot",
        "Avoid forcing trades in weak or overextended conditions"
      ]
    },
    botContext: {
      selectedBotType: { type: "string", value: config.selectedBotType, meaning: "Active entry style used by the bot" },
      selectedMode: { type: "string", value: config.selectedMode, meaning: "Active trade style / exit behavior" },
      signalTimeframe: { type: "string", value: config.signalTimeframe, example: "5min", meaning: "Main signal timeframe for entries" },
      trendTimeframe: { type: "string", value: config.trendTimeframe, example: "1h", meaning: "Trend confirmation timeframe" },
      minScalpTargetPct: { type: "number", value: config.minScalpTargetPct, example: 0.008, meaning: "Minimum scalp target expected from a setup" },
      maxScalpTargetPct: { type: "number", value: config.maxScalpTargetPct, example: 0.02, meaning: "Maximum scalp target expected from a setup" },
      timeStopMinutes: { type: "number", value: config.timeStopMinutes, example: 15, meaning: "Trade should not stay alive too long without progress" },
      maxHoldMinutes: { type: "number", value: config.maxHoldMinutes, example: 60, meaning: "Maximum acceptable hold duration" },
      breakEvenMinutes: { type: "number", value: config.breakEvenMinutes, example: 12, meaning: "When break-even protection becomes relevant" },
      minConfirmation: { type: "number", value: config.minConfirmation, example: 3, meaning: "Required confirmation count before entry" },
      breakoutPct: { type: "number", value: config.breakoutPct, example: 0.002, meaning: "Breakout threshold used by the entry logic" },
      requireBreakout: { type: "boolean", value: config.requireBreakout, example: false, meaning: "Only allow entries when breakout confirmation is present" },
      requireRsiMomentum: { type: "boolean", value: config.requireRsiMomentum, example: false, meaning: "RSI momentum filter must confirm the setup" },
      enableAtrFilter: { type: "boolean", value: config.enableAtrFilter, example: false, meaning: "ATR volatility filter is active" },
      enableVolumeFilter: { type: "boolean", value: config.enableVolumeFilter, example: false, meaning: "Volume quality filter is active" }
    },
    marketProfiles: Object.fromEntries(Object.entries(config.marketProfiles || {}).map(([key, value]) => [key, {
      allowEntries: { type: "boolean", value: value.allowEntries !== false, meaning: "Whether entries are allowed in this profile" },
      entryOverrides: Object.fromEntries(Object.entries(value.entryOverrides || {}).map(([field, fieldValue]) => [field, {
        type: typeof fieldValue === "boolean" ? "boolean" : typeof fieldValue === "number" ? "number" : "string",
        value: fieldValue,
        example: typeof fieldValue === "boolean" ? !fieldValue : fieldValue,
        meaning: field
      }]))
    }])),
    rankedCandidates: candidates.slice(0, 20).map((item, index) => ({
      rank: index + 1,
      symbol: item.symbol,
      score: item.score,
      quoteVol: item.quoteVol,
      changePct: item.changePct,
      rangePct: item.rangePct,
      last: item.last,
      activeNow: (rotation.activePairs || []).includes(item.symbol)
    })),
    minimalGlobalContext: {
      candidateCount: candidates.length,
      activePairCount: (rotation.activePairs || []).length,
      averageCandidateChangePct: candidates.length ? Number((candidates.reduce((sum, item) => sum + (Number(item.changePct) || 0), 0) / candidates.length).toFixed(4)) : 0,
      averageCandidateRangePct: candidates.length ? Number((candidates.reduce((sum, item) => sum + (Number(item.rangePct) || 0), 0) / candidates.length).toFixed(4)) : 0,
      rotationTopPairs: rotation.topPairs || 15,
      rotationCategories: rotation.activeCategories || "-"
    },
    constraints: {
      allowedChanges: ["marketProfile", "allowEntries", "entryOverrides", "reason"],
      forbiddenChanges: ["riskPercent", "sizing", "pair list", "bot type", "trade mode", "TP", "SL", "cooldown"]
    },
    expectedOutputSchema: {
      marketProfile: "custom",
      allowEntries: true,
      entryOverrides: {
        minExpectedNetPct: 0.0026,
        minVolumeRatio: 1.08,
        minTrendRsi: 40,
        minAtrPct: 0.0028,
        maxAtrPct: 0.02,
        maxEmaGapPct: 0.015,
        requireRsiMomentum: true,
        requireBreakout: true
      },
      reason: "short reason"
    }
  };

  const prompt = [
    "Return one valid JSON object only. No markdown. No extra text.",
    "Read the following full structured trading context and decide the best market profile action.",
    JSON.stringify(richContext, null, 2)
  ].join("\n\n");

  const res = await axios.post(
    "https://openrouter.ai/api/v1/chat/completions",
    {
      model,
      messages: [
        { role: "system", content: "You are a senior crypto spot trader in 2026. Return one valid JSON object only." },
        { role: "user", content: prompt }
      ],
      response_format: { type: "json_object" },
      max_tokens: 5000,
      temperature: 0.2
    },
    {
      timeout: 30000,
      proxy: false,
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
        "HTTP-Referer": "https://airplanestar.local",
        "X-Title": "airplanestar-bot"
      }
    }
  );

  const rawText = res.data?.choices?.[0]?.message?.content || "";
  const parsed = aiAgent.__parseJsonForTest(rawText);
  let validated = null;
  let validationError = null;
  try {
    validated = aiAgent.validateDecision(parsed, aiAgent.getAiAgentSettings(config));
  } catch (err) {
    validationError = err.message;
  }

  console.log(JSON.stringify({
    provider: res.data?.provider,
    model: res.data?.model,
    finish_reason: res.data?.choices?.[0]?.finish_reason,
    rawText,
    parsed,
    validated,
    validationError
  }, null, 2));
}

main().catch((err) => {
  console.error(err.response?.data || err.message || err);
  process.exit(1);
});
